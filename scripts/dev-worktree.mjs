#!/usr/bin/env node

import { createHash, randomBytes } from 'node:crypto'
import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import net from 'node:net'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'
import { spawn } from 'node:child_process'

const root = dirname(dirname(fileURLToPath(import.meta.url)))
const webDir = join(root, 'apps', 'web')
const rootEnvPath = join(root, '.env.development.local')
const webEnvPath = join(webDir, '.env.local')
const stateDir = join(root, '.dev')
const statePath = join(stateDir, 'worktree-env.json')

const command = process.argv[2] || 'start'
const hash = createHash('sha1').update(root).digest('hex').slice(0, 8)
const slug = `lawallet_${hash}`
const composeProject = `lawallet-nwc-${hash}`

function parseEnvFile(path) {
  if (!existsSync(path)) return {}

  return Object.fromEntries(
    readFileSync(path, 'utf8')
      .split(/\r?\n/)
      .map(line => line.trim())
      .filter(line => line && !line.startsWith('#') && line.includes('='))
      .map(line => {
        const index = line.indexOf('=')
        const key = line.slice(0, index)
        const raw = line.slice(index + 1).trim()
        const value = raw.replace(/^"(.*)"$/, '$1').replace(/^'(.*)'$/, '$1')
        return [key, value]
      })
  )
}

function renderEnv(env) {
  return `${[
    '# Generated by pnpm start:dev-server. Safe to delete and regenerate.',
    `DEV_WORKTREE_ROOT="${root}"`,
    `DEV_COMPOSE_PROJECT="${env.DEV_COMPOSE_PROJECT}"`,
    `POSTGRES_DB="${env.POSTGRES_DB}"`,
    `POSTGRES_USER="${env.POSTGRES_USER}"`,
    `POSTGRES_PASSWORD="${env.POSTGRES_PASSWORD}"`,
    `POSTGRES_PORT="${env.POSTGRES_PORT}"`,
    `POSTGRES_VOLUME="${env.POSTGRES_VOLUME}"`,
    `DATABASE_URL="${env.DATABASE_URL}"`,
    'NODE_ENV="development"',
    `PORT="${env.PORT}"`,
    `WEB_PORT="${env.WEB_PORT}"`,
    `JWT_SECRET="${env.JWT_SECRET}"`,
    'AUTO_GENERATE_ALBY_SUBACCOUNTS="false"',
    'MAINTENANCE_MODE="false"',
    'RATE_LIMIT_ENABLED="false"',
    'LOG_LEVEL="debug"',
    'LOG_PRETTY="true"',
    'NEXT_PUBLIC_LAWALLET_LANDING_URL="/admin"'
  ].join('\n')}\n`
}

async function isPortFree(port) {
  return new Promise(resolve => {
    const server = net.createServer()
    server.once('error', () => resolve(false))
    server.once('listening', () => {
      server.close(() => resolve(true))
    })
    server.listen(port, '127.0.0.1')
  })
}

async function findPort(start) {
  for (let port = start; port < start + 200; port += 1) {
    if (await isPortFree(port)) return String(port)
  }

  throw new Error(`No free port found starting at ${start}`)
}

async function loadOrCreateEnv() {
  const existing = parseEnvFile(rootEnvPath)
  const state = existsSync(statePath)
    ? JSON.parse(readFileSync(statePath, 'utf8'))
    : {}

  const postgresPort =
    existing.POSTGRES_PORT ||
    state.POSTGRES_PORT ||
    (await findPort(15432 + (parseInt(hash.slice(0, 4), 16) % 1000)))
  const webPort =
    existing.WEB_PORT ||
    existing.PORT ||
    state.WEB_PORT ||
    (await findPort(3000 + (parseInt(hash.slice(4, 8), 16) % 1000)))
  const postgresUser = existing.POSTGRES_USER || state.POSTGRES_USER || slug
  const postgresDb = existing.POSTGRES_DB || state.POSTGRES_DB || slug
  const postgresPassword =
    existing.POSTGRES_PASSWORD ||
    state.POSTGRES_PASSWORD ||
    randomBytes(24).toString('base64url')

  const env = {
    DEV_COMPOSE_PROJECT:
      existing.DEV_COMPOSE_PROJECT || state.DEV_COMPOSE_PROJECT || composeProject,
    POSTGRES_DB: postgresDb,
    POSTGRES_USER: postgresUser,
    POSTGRES_PASSWORD: postgresPassword,
    POSTGRES_PORT: postgresPort,
    POSTGRES_VOLUME:
      existing.POSTGRES_VOLUME ||
      state.POSTGRES_VOLUME ||
      `${composeProject}_postgres_data`,
    DATABASE_URL: `postgresql://${postgresUser}:${postgresPassword}@localhost:${postgresPort}/${postgresDb}`,
    NODE_ENV: 'development',
    PORT: webPort,
    WEB_PORT: webPort,
    JWT_SECRET:
      existing.JWT_SECRET ||
      state.JWT_SECRET ||
      randomBytes(48).toString('base64url')
  }

  mkdirSync(stateDir, { recursive: true })
  writeFileSync(statePath, `${JSON.stringify(env, null, 2)}\n`)
  writeFileSync(rootEnvPath, renderEnv(env))
  writeFileSync(webEnvPath, renderEnv(env))

  return env
}

function run(name, args, options = {}) {
  return new Promise((resolve, reject) => {
    const child = spawn(name, args, {
      cwd: options.cwd || root,
      env: { ...process.env, ...options.env },
      stdio: options.stdio || 'inherit',
      shell: false
    })

    child.once('error', reject)
    child.once('exit', code => {
      if (code === 0) {
        resolve()
        return
      }

      reject(new Error(`${name} ${args.join(' ')} exited with ${code}`))
    })
  })
}

function shellQuote(value) {
  return `'${String(value).replace(/'/g, `'\\''`)}'`
}

function pnpmCommand(args) {
  const nvmPath = join(process.env.HOME || '', '.nvm', 'nvm.sh')
  const nvmPrefix = existsSync(nvmPath)
    ? `source ${shellQuote(nvmPath)} && nvm use --silent >/dev/null && `
    : ''

  return `${nvmPrefix}pnpm ${args.map(shellQuote).join(' ')}`
}

function runPnpm(args, options = {}) {
  return run('bash', ['-lc', pnpmCommand(args)], options)
}

function runPnpmAttached(args, options = {}) {
  const child = spawn('bash', ['-lc', pnpmCommand(args)], {
    cwd: options.cwd || root,
    env: { ...process.env, ...options.env },
    stdio: 'inherit',
    shell: false
  })

  for (const signal of ['SIGINT', 'SIGTERM']) {
    process.on(signal, () => {
      child.kill(signal)
    })
  }

  child.once('exit', code => process.exit(code || 0))
}

async function waitForPostgres(env) {
  for (let attempt = 1; attempt <= 30; attempt += 1) {
    const check = spawn(
      'docker',
      [
        'compose',
        '--project-name',
        env.DEV_COMPOSE_PROJECT,
        'exec',
        '-T',
        'postgres',
        'pg_isready',
        '-U',
        env.POSTGRES_USER,
        '-d',
        env.POSTGRES_DB
      ],
      { cwd: root, env: { ...process.env, ...env }, stdio: 'ignore' }
    )

    const healthy = await new Promise(resolve => {
      check.once('exit', code => resolve(code === 0))
      check.once('error', () => resolve(false))
    })

    if (healthy) return
    await new Promise(resolve => setTimeout(resolve, 1000))
  }

  throw new Error('Postgres did not become ready in time')
}

async function isFreshDatabase(env) {
  // A database is "fresh" until Prisma has applied at least one migration.
  // Checked via the managed compose container so no local psql is needed.
  const check = spawn(
    'docker',
    [
      'compose',
      '--project-name',
      env.DEV_COMPOSE_PROJECT,
      'exec',
      '-T',
      'postgres',
      'psql',
      '-U',
      env.POSTGRES_USER,
      '-d',
      env.POSTGRES_DB,
      '-tAc',
      "SELECT to_regclass('public._prisma_migrations') IS NULL"
    ],
    { cwd: root, env: { ...process.env, ...env }, stdio: ['ignore', 'pipe', 'ignore'] }
  )

  let output = ''
  check.stdout.on('data', chunk => {
    output += chunk
  })

  return new Promise(resolve => {
    check.once('error', () => resolve(true))
    check.once('exit', code => resolve(code !== 0 || output.trim() === 't'))
  })
}

async function bootstrap(env, { reset }) {
  if (!existsSync(join(root, 'node_modules', '.modules.yaml'))) {
    console.log('Installing workspace dependencies...')
    await runPnpm(['install', '--frozen-lockfile'], { env })
  }

  console.log(`Starting isolated Postgres for ${env.DEV_COMPOSE_PROJECT}...`)
  await run(
    'docker',
    ['compose', '--project-name', env.DEV_COMPOSE_PROJECT, 'up', '-d', 'postgres'],
    { env }
  )
  await waitForPostgres(env)

  console.log('Generating Prisma client...')
  await runPnpm(['--filter', '@lawallet-nwc/web', 'exec', 'prisma', 'generate'], {
    env
  })

  const fresh = await isFreshDatabase(env)

  if (!reset && !fresh) {
    console.log('Applying pending migrations without resetting data...')
    await runPnpm(
      ['--filter', '@lawallet-nwc/web', 'exec', 'prisma', 'migrate', 'deploy'],
      { env }
    )
    return
  }

  if (fresh) {
    console.log('Fresh database: migrating and seeding...')
    await runPnpm(
      ['--filter', '@lawallet-nwc/web', 'exec', 'prisma', 'migrate', 'deploy'],
      { env }
    )
  } else {
    console.log('Resetting and migrating the worktree database...')
    await runPnpm(
      [
        '--filter',
        '@lawallet-nwc/web',
        'exec',
        'prisma',
        'migrate',
        'reset',
        '--force',
        '--skip-seed'
      ],
      { env }
    )
  }

  await runPnpm(['--filter', '@lawallet-nwc/web', 'run', 'seed'], { env })
}

async function main() {
  const env = await loadOrCreateEnv()

  if (command === 'env') {
    console.log(`Environment written to:\n- ${rootEnvPath}\n- ${webEnvPath}`)
    console.log(`Web URL: http://localhost:${env.WEB_PORT}/admin`)
    return
  }

  if (
    command === 'setup' ||
    command === 'bootstrap' ||
    command === 'start' ||
    command === 'reset-db'
  ) {
    // `reset-db` (and the legacy DEV_DB_RESET=true escape hatch) wipes data;
    // everything else seeds a fresh database and only migrates an existing one.
    const reset = command === 'reset-db' || process.env.DEV_DB_RESET === 'true'
    await bootstrap(env, { reset })
  } else {
    throw new Error(`Unknown command: ${command}`)
  }

  console.log(`Ready: http://localhost:${env.WEB_PORT}/admin`)

  if (command === 'start') {
    runPnpmAttached(
      ['--dir', 'apps/web', 'exec', 'next', 'dev', '--port', env.WEB_PORT],
      { env }
    )
  }
}

main().catch(error => {
  console.error(error)
  process.exit(1)
})
